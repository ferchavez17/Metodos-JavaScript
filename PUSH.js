// Array que contiene las tareas de la lista
let listaTareas = ['Completar informe trimestral', 'Ir al supermercado', 'Hacer ejercicio'];

// Nueva tarea introducida por el usuario
let nuevaTarea = 'Llamar al cliente para confirmar la reunión';

// Utilizando el método push para agregar la nueva tarea a la lista de tareas
listaTareas.push(nuevaTarea);

console.log(`Se ha añadido una nueva tarea a la lista: ${nuevaTarea}`);
console.log('La lista de tareas actualizada es:');
console.log(listaTareas);