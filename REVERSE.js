// Símbolos encriptados en la pizarra
let simbolosEncriptados = ["#","$","%","&","*","@"];

// Aplicando el método reverse para revertir el orden de los símbolos
let simbolosDesencriptados = simbolosEncriptados.reverse();

// Mostrando los símbolos desencriptados
console.log(simbolosDesencriptados); // Resultado: ["@","*","&","%","$","#"]
